    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int id = (Integer) tblTurlar.getModel().getValueAt(tblTurlar.convertRowIndexToModel(selectedRow), 0);
        Connection connection = null;
        DbHelper helper = new DbHelper();
        PreparedStatement statement = null;
        Object[] options = {"Sil", "İptal et"};
        int n = JOptionPane.showOptionDialog(optCancelContinue,
                "Bu turu silmek istediğinize emin misiniz?",
                "Emin misiniz?",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                options,
                options[0]);
        switch (n) {
            case JOptionPane.YES_OPTION:
                try {
                connection = helper.getConnection();
                String sql = "DELETE from turlar WHERE ID=" + id;
                statement = connection.prepareStatement(sql);
                statement.executeUpdate();
                JOptionPane.showMessageDialog(null, "Tur silindi", "İşlem Başarılı", JOptionPane.INFORMATION_MESSAGE);
                populateTable();

            } catch (SQLException ex) {
                Logger.getLogger(frmMusteriGoruntule.class.getName()).log(Level.SEVERE, null, ex);
            } finally {
                try {
                    statement.close();
                    connection.close();
                } catch (SQLException ex) {
                    Logger.getLogger(frmMusteriEkle.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            break;
            case JOptionPane.NO_OPTION:
                break;
            default:
                break;
        }


    }//GEN-LAST:event_jButton3ActionPerformed

