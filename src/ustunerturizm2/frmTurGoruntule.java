    public void populateTable() {
        dfmodel.setRowCount(0);
        dfmodel = (DefaultTableModel) tblTurlar.getModel();
        try {
            ArrayList<Tur> turlar = getTur();
            for (Tur tur : turlar) {
                Object[] row = {
                    tur.getId(),
                    tur.getTuradi(),
                    tur.getHareket_tarihi(),
                    tur.getDonus_tarihi(),
                    tur.getHareket_saati(),
                    tur.getHareket_yeri(),
                    tur.getTur_danismani(),
                    tur.getTur_otobusu_id()
                };
                dfmodel.addRow(row);

            }

        } catch (SQLException ex) {
            DbHelper help = new DbHelper();
            help.showErrorMessage(ex);
        }
        if (tblTurlar.getColumnName(0).equals("ID")) {
            tblTurlar.removeColumn(tblTurlar.getColumn("ID"));
            tblTurlar.removeColumn(tblTurlar.getColumn("turotobusid"));
        }

    }

