    public ArrayList<Tur> getGoruntulenenTur() throws SQLException {
        int id = (Integer) tblTurlar.getModel().getValueAt(tblTurlar.convertRowIndexToModel(selectedRow), 0);
        Connection connection = null;
        DbHelper helper = new DbHelper();
        Statement statement = null;
        ResultSet resultSet;
        ArrayList<Tur> GoruntulenenTur = null;
        try {
            connection = helper.getConnection();
            statement = connection.createStatement();
            resultSet = statement.executeQuery("Select * from turlar WHERE ID=" + id);
            GoruntulenenTur = new ArrayList<>();
            while (resultSet.next()) {
                GoruntulenenTur.add(new Tur(resultSet.getInt("ID"),
                        resultSet.getString("turadi"),
                        resultSet.getString("hareket_tarihi"),
                        resultSet.getString("donus_tarihi"),
                        resultSet.getString("hareket_saati"),
                        resultSet.getString("hareket_yeri"),
                        resultSet.getString("tur_danismani"),
                        resultSet.getInt("tur_otobusu_id"))
                );

            }
        } catch (SQLException e) {
            helper.showErrorMessage(e);

        } finally {
            statement.close();
            connection.close();
        }
        return GoruntulenenTur;
    }
